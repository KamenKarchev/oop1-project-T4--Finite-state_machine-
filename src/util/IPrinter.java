package util;

/**
 * <h2>Интерфейс за печат (utility)</h2>
 *
 * <p>
 * Минимален интерфейс за обекти, които могат да „се отпечатат“ (например към конзола).
 * Използва се като общ контракт там, където искаме да отделим форматирането/извеждането
 * от конкретните типове.
 * </p>
 */
public interface IPrinter {
    /**
     * Извежда представяне на обекта (конкретният изход зависи от имплементацията).
     */
    void print();
}
