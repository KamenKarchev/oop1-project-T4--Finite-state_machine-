/**
 * <h2>Конкретни фабрики за команди</h2>
 *
 * <p>
 * В този пакет се намират конкретните реализации на {@link cli.command.factory.CommandFactory}.
 * Всяка фабрика отговаря за точно един {@link cli.command.CommandToken} и връща конкретна команда
 * (клас от {@code cli.command.commands.*}).
 * </p>
 *
 * <h3>Защо „по една фабрика на команда“?</h3>
 *
 * <p>
 * Този стил е умишлено „многословен“, но има практични предимства:
 * </p>
 *
 * <ul>
 *   <li>Командите се добавят/махат без да се пипат големи {@code switch} блокове.</li>
 *   <li>Лесно се подменя логиката на дадена команда (сменяш само съответния клас/фабрика).</li>
 *   <li>
 *     Контекстът {@link cli.runtime.CliContext} се инжектира централизирано, което прави тестове и
 *     повторна употреба по-лесни.
 *   </li>
 * </ul>
 */
