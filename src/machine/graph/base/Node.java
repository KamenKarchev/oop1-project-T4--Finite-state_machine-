package graph.base;

import graph.mdg.MDGEdge;

import java.util.Set;

/**
 * <h2>Възел в граф</h2>
 *
 * <p>
 * Възелът представя състояние в автомата и съдържа множеството от изходящи ребра
 * (преходи към други възли).
 * </p>
 *
 * <p>
 * Равенството на възлите обикновено е дефинирано по име (виж конкретната имплементация).
 * </p>
 *
 * @param <E> тип на ребрата
 */
public interface Node<E extends Edge<?>> {
    /**
     * @return име/идентификатор на възела (състоянието)
     */
    String getName();

    /**
     * @return множество от изходящи ребра от този възел
     */
    Set<E> getEdges();
}
